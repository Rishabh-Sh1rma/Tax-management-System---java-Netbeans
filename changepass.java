        String uid=user.getText();
        String old = oldpass.getText();
        String newp=newpass.getText();
        String conp=conpass.getText();// TODO add your handling code here:
        try
        { 
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/taxmanagement", "root", "root");
        String sql ="SELECT pass FROM sign WHERE user='"+uid+"'";
        String sql1 = "update sign set pass= '"+newp+"' where user='"+uid+"'";
        pst=con.prepareCall(sql);
        rs=pst.executeQuery();
        if ((rs.next())){
            udl.setText("");
            if(rs.getString("pass").equals(old))
                
                { oldl.setText("");
                  if(newp.equals(conp)){
                      cnl.setText("");
                      PreparedStatement stmt = con.prepareStatement(sql1);
                      stmt.executeUpdate();
                       JOptionPane.showMessageDialog(rootPane, "Password changed successfully!");
                  }
                  else{
                      cnl.setText("password didn't matched!");
                      
                      }
                }
              else{
                          oldl.setText("Invalid old password!");
                          }
            
       
        }
        else{
            udl.setText("Invalid User ID.");
        }
        con.close();
        }
